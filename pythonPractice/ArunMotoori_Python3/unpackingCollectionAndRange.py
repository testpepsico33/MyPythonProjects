#Unpacking List
a,b,c,d=[9,1,5,7]

print(a)
print(b)
print(c)
print(d)

#Unpacking Tuple
a,b,c,d=(9,1,5,7)

print(a)
print(b)
print(c)
print(d)

#Unpacking Set
a,b,c,d={9,1,5,7}

print(a)
print(b)
print(c)
print(d)

#Unpacking Dictionary
s={"name":"Nirmal","Experience":6,"Location":"Bangalore","State":"Karnataka"}

a,b,c,d=s.values()
print(a)
print(b)
print(c)
print(d)

#Unpacking Range
a,b,c,d=range(4) #0,1,2,3
print(a)
print(b)
print(c)
print(d)




